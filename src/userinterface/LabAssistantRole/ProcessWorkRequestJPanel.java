    public void populateTable() {

        LabTest labTest = request.getLabTest();

        DefaultTableModel model = (DefaultTableModel) tblTestDetails.getModel();

        model.setRowCount(0);

        for (TestDetails testDetails : labTest.getTestDetails()) {
            //        MED,DOSE,FREQ,NUM OF DAYS,CONDITION,NOTES
            Object[] row = new Object[5];
            row[0] = testDetails;
            row[1] = testDetails.getResult();
            row[2] = testDetails.getRefRange();
            row[3] = testDetails.getUnit();
            row[4] = testDetails.getObservation();

            model.addRow(row);

        }

    }

    public void addListnerToLabTestTable() {

        tblTestDetails.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent event) {
                if (tblTestDetails.getSelectedRow() > -1) {
                    int selectedRow = tblTestDetails.getSelectedRow();
                    TestDetails testDetails = (TestDetails) tblTestDetails.getValueAt(selectedRow, 0);

                    txtTestName.setText(testDetails.getTestName());
                    txtRefRange.setText(testDetails.getRefRange());
                    txtResult.setText(testDetails.getResult());
                    txtObservation.setText(testDetails.getObservation());
                    txtUnit.setText(testDetails.getUnit());

                }
            }

        });

    }

