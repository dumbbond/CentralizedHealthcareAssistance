    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (tblTestDetails.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Please select atleast 1 row from table!");
            return;
        }
        int selectedRow = tblTestDetails.getSelectedRow();

        TestDetails testDetails = (TestDetails) tblTestDetails.getValueAt(selectedRow, 0);

        try {

            String RefRange = txtRefRange.getText();
            String Result = txtResult.getText();
            String Observation = txtObservation.getText();
            String Unit = txtUnit.getText();

            if (RefRange.equals("") || Result.equals("") || Observation.equals("") || Unit.equals("")) {
                JOptionPane.showMessageDialog(null, "All input fields are mandatory!");
                return;
            }

            testDetails.setObservation(Observation);
            testDetails.setRefRange(RefRange);
            testDetails.setResult(Result);
            testDetails.setUnit(Unit);
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please check all input fields and try again!");
            return;
        } finally {
            txtTestName.setText("");
            txtRefRange.setText("");
            txtResult.setText("");
            txtObservation.setText("");
            txtUnit.setText("");
            populateTable();
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

