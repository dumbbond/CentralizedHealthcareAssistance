    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:
        
        int selectedRowCount = tblOrganization.getSelectedRowCount();
        int selectedRow = tblOrganization.getSelectedRow();
        if (selectedRowCount == 1){
            CAPatientAuthorizationRequest req = (CAPatientAuthorizationRequest)tblOrganization.getValueAt(selectedRow, 5);

            if(account.getWorkQueue().getWorkRequestList().contains(req)){
                JOptionPane.showMessageDialog(null, "Request has already been assigned to you");
                return;
            }
            
            if (req.getReceiver() != null){
                JOptionPane.showMessageDialog(null, "Request has already been assigned to " + req.getReceiver().getEmployee().getName());
                return; 
            }
            
            if (req.getStatus().equalsIgnoreCase("Completed")){
                JOptionPane.showMessageDialog(null, "Completed request can't be assigned to you");
                return;
            }
            req.setReceiver(account);
            account.getWorkQueue().getWorkRequestList().add(req);
            loadOrganizationQue();
            loadSelfQue();
        }
        else
        {JOptionPane.showMessageDialog(null, "Please select one row");}
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnAssignToPoolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToPoolActionPerformed
        // TODO add your handling code here:
        int selectedRowCount = tblMyTask.getSelectedRowCount();
        int selectedRow = tblMyTask.getSelectedRow();
        if (selectedRowCount == 1){
            CAPatientAuthorizationRequest req = (CAPatientAuthorizationRequest)tblMyTask.getValueAt(selectedRow, 5);
            
            if (this.req == req){
                int dialogResult = JOptionPane.showConfirmDialog (null, "Would you like to send open request back to the the pool");
                if(dialogResult == JOptionPane.YES_OPTION){
                  emptyView(); 
                } else {return;}
            }
            
            req.setReceiver(null);
            account.getWorkQueue().getWorkRequestList().remove(req);
            loadOrganizationQue();
            loadSelfQue();
        }
        else
        {JOptionPane.showMessageDialog(null, "Please select one row");}
     
        
    }//GEN-LAST:event_btnAssignToPoolActionPerformed

