    public void loadOrganizationQue(){
    
            DefaultTableModel dtm = (DefaultTableModel)tblOrganization.getModel(); 
            dtm.setRowCount(0);
        for (WorkRequest request: organization.getWorkQueue().getWorkRequestList()){
            if (request instanceof CAPatientAuthorizationRequest){
              
                Object[] row = new Object[dtm.getColumnCount()];
                CAPatientAuthorizationRequest req = ((CAPatientAuthorizationRequest)request);
                row[0]= req.getMessage();
                row[1]=req.getSender();
                row[2]= req.getReceiver();
                row[3]=req.getRequestDate();
                row[4]=req.getStatus();
                row[5]=req;
                
                dtm.addRow(row);
                        
            }
        }
    
    }
    
    
