    private void btnApprovedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApprovedActionPerformed
        // TODO add your handling code here:
        
        patient.setStatus("Approved");
        for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
            if (org instanceof CAPatientManagementOrganization){
                ((CAPatientManagementOrganization)org).getPatientDirectory().addPatient(patient);
            }
        }
        req.setStatus("Completed");
        account.getWorkQueue().getWorkRequestList().remove(req);
        loadOrganizationQue();
        loadSelfQue();
        String message = "Patient: "+ patient.getName() + " with patient ID: " + patient.getPatientId() + " request has been Approved." ;
        try{
        Email.sendEmail(patient.getEmail(), "Patient addition request Approved", message);}
        catch (Exception e){
            System.out.println(e);
        }finally{
            emptyView();
        }
    }//GEN-LAST:event_btnApprovedActionPerformed

    private void btnDeclineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclineActionPerformed
        // TODO add your handling code here:
        if ((txtDeclineReason.getText().equalsIgnoreCase("") || txtDeclineReason.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Kindly enter decline reason");
            return;
        }
        String reason = "Decline: " + txtDeclineReason.getText();
        patient.setStatus(reason);
        for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){
            if (org instanceof CAPatientManagementOrganization){
                ((CAPatientManagementOrganization)org).getPatientDirectory().addPatient(patient);
            }
        }
        req.setStatus("Completed");
        account.getWorkQueue().getWorkRequestList().remove(req);
        loadOrganizationQue();
        loadSelfQue();
        String message = "Patient: "+ patient.getName() + " with patient ID: " + patient.getPatientId() + " request has been declined due to " + txtDeclineReason.getText();
        try{
        Email.sendEmail(patient.getEmail(), "Patient addition request declined", message);}
        catch (Exception e){
            System.out.println(e);
        }finally{
            emptyView();
        }
    }//GEN-LAST:event_btnDeclineActionPerformed

