    private void btnLoginClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginClientActionPerformed
        // TODO add your handling code here:
        
        try{
        int i = Integer.parseInt(txtClientUserName.getText());
        char[] otpChar = txtClientOTP.getPassword();
        String otp = String.valueOf(otpChar);
        int j  = Integer.parseInt(otp);
        }catch (NumberFormatException n){
            JOptionPane.showMessageDialog(null, "Patient ID and OTP can only be integer");
            return;
        }
        
        int patientID = Integer.parseInt(txtClientUserName.getText());
        // Get Password
        char[] otpChar = txtClientOTP.getPassword();
        String otp = String.valueOf(otpChar);
        
        CAFetchPatientRequest request = new CAFetchPatientRequest();
        request.setPatient(null);
        request.setMessage("Patient Login OTP Verification");
        request.setPatientId(patientID);
        request.setOtp(Integer.parseInt(otp));
        request.setRequestDate(new Date());
        request.setStatus("Pending");
        
        if (OTPVerification.fetchPatient(system, request)){
        if (!request.getPatient().getStatus().equalsIgnoreCase("Approved")){
            JOptionPane.showMessageDialog(null, "You cannot view your account until its approved");
            txtClientOTP.setText("");
            txtClientUserName.setText("");
            return;
        }
            
        logoutJButton.setEnabled(false);
        userNameJTextField.setEnabled(false);
        passwordField.setEnabled(false);
        loginJButton.setEnabled(false);
        txtClientOTP.setEnabled(false);
        txtClientUserName.setEnabled(false);
        btnLoginClient.setEnabled(false);
        btnLogoutClient.setEnabled(true);
        btnRequestOTP.setEnabled(false);
            Patient patient = request.getPatient();
            PatientProfileJPanel panel = new PatientProfileJPanel(patient, container, system);
            CardLayout layout=(CardLayout)container.getLayout();
            container.add("patientWorkArea",panel);
            layout.next(container);
        }else{
          JOptionPane.showMessageDialog(null, "OTP not authenticated");
          txtClientOTP.setText("");
        }
        
    }//GEN-LAST:event_btnLoginClientActionPerformed

